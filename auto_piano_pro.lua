-- ─── Key input backend ──────────────────────────────────────────────────
-- Try multiple executor backends; fall back to firing the piano remote directly.
local backend = nil         -- "keypress" | "remote"
local Connector = workspace:FindFirstChild("GlobalPianoConnector")

local function detectBackend()
  if type(keypress) == "function" and type(keyrelease) == "function" then
    backend = "keypress"
    return "keypress"
  end
  if Connector and Connector:IsA("RemoteEvent") then
    backend = "remote"
    return "remote"
  end
  -- last resort: VirtualInputManager
  local ok = pcall(function()
    local v = game:GetService("VirtualInputManager")
    if v then backend = "vim"; end
  end)
  return backend
end
detectBackend()

-- Map a note number (1..61) back to the keyboard key character, then to a KeyCode.
local function noteToKeyCode(note)
  local ch = string.sub(KEYMAP, note, note)
  -- ASCII value of ch. a-z=97-122, 0-9=48-57, symbols vary.
  local code = string.byte(ch)
  -- KeyCode.Value equals the ASCII code for letter/digit keys in Roblox.
  return code, ch
end

-- Play a single note number. Returns true on success.
local function playNote(note)
  if note < 1 or note > 61 then return false end
  if backend == "remote" and Connector then
    -- Fire the piano remote directly (server replicates the note to everyone).
    pcall(function() Connector:FireServer("play", note) end)
    -- Also trigger the client-side sound + key highlight so YOU hear/see it.
    local gui = LocalPlayer.PlayerGui:FindFirstChild("PianoGui")
    if gui then
      local main = gui:FindFirstChild("PianoGui")
      if main then
        local keys = main:FindFirstChild("Keys")
        if keys and keys:FindFirstChild(tostring(note)) then
          local k = keys[tostring(note)]
          k.BackgroundColor3 = (note % 12 == 2 or note % 12 == 4 or note % 12 == 7 or note % 12 == 9 or note % 12 == 11)
            and Color3.fromRGB(50, 50, 50) or Color3.fromRGB(200, 200, 200)
          task.delay(0.4, function()
            if k then k.BackgroundColor3 = (note % 12 == 2 or note % 12 == 4 or note % 12 == 7 or note % 12 == 9 or note % 12 == 11)
              and Color3.fromRGB(0, 0, 0) or Color3.fromRGB(255, 255, 255) end
          end)
        end
      end
    end
    return true
  end
  if backend == "keypress" then
    local code, ch = noteToKeyCode(note)
    -- Use ASCII code as KeyCode.
    local kc = Enum.KeyCode[code] -- may be nil for symbols; handle below
    if kc then
      pcall(keypress, code)
      task.wait(Config.keyHold)
      pcall(keyrelease, code)
    else
      -- symbols: fire remote if available as fallback
      if Connector then pcall(function() Connector:FireServer("play", note) end) end
    end
    return true
  end
  -- vim fallback
  if backend == "vim" then
    local code, ch = noteToKeyCode(note)
    pcall(function()
      local v = game:GetService("VirtualInputManager")
      v:SendKeyEvent(true, code, false)
      task.wait(Config.keyHold)
      v:SendKeyEvent(false, code, false)
    end)
    return true
  end
  return false
end

-- ─── RPS parser ────────────────────────────────────────────────────────
-- RPS format (playpianosheets.com / virtual piano):
--   - Whitespace separates notes/chords.
--   - [abc] = chord: notes a,b,c played together (rolled).
--   - A single char = a note.
--   - Newlines / "\" are phrase separators (longer rest).
--   - "-" / "–" / "—" are legato/sustain markers (we treat as a held note -> ignore as separator).
--   - "[" and "]" bracket chords.
-- Tokens that are NOT in KEYMAP (e.g. shift-only symbols) are skipped.
local function parseRPS(content)
  -- Replace various dash glyphs with empty (they mean sustain, not a rest),
  -- but keep them from creating spurious notes.
  local text = content
    :gsub("\r", "")
    :gsub("\\", "\n")      -- literal backslash-n in some sheets
    :gsub("–", "-")           -- en dash
    :gsub("—", "-")           -- em dash
    :gsub("”", "")
    :gsub("“", "")
  -- Split into tokens by whitespace, but keep chord brackets intact.
  local tokens = {}
  -- We iterate char by char building tokens.
  local i = 1
  local len = #text
  while i <= len do
    local c = text:sub(i, i)
    if c == "[" then
      -- read until ]
      local j = text:find("]", i, true)
      if j then
        local inside = text:sub(i + 1, j - 1)
        table.insert(tokens, { chord = true, text = inside })
        i = j + 1
      else
        i = i + 1
      end
    elseif c == "]" then
      i = i + 1
    elseif c == " " or c == "\t" then
      i = i + 1
    elseif c == "\n" then
      table.insert(tokens, { break_ = true })
      i = i + 1
    elseif c == "-" then
      -- sustain marker; skip (don't emit a note, no rest either)
      i = i + 1
    else
      -- single char note (could be junk like "–" already replaced)
      table.insert(tokens, { note = c })
      i = i + 1
    end
  end
  return tokens
end

-- Convert a chord/string of chars into note numbers, applying transpose.
local function charsToNotes(str, transpose)
  local notes = {}
  for k = 1, #str do
    local ch = str:sub(k, k)
    if ch ~= " " and ch ~= "\t" then
      local base = charToNote(ch)
      if base then
        local n = base + (transpose or 0)
        if n >= 1 and n <= 61 then
          table.insert(notes, n)
        end
      end
    end
  end
  return notes
end

-- ─── Player core ───────────────────────────────────────────────────────
local function playSongBySlug(slug)
  local song = SONGS[slug]
  if not song then
    warn("[AutoPiano] Unknown song: " .. tostring(slug))
    return false
  end
  State.currentSong = slug
  State.playing = true
  State.stopRequested = false

  local transpose = (song.transpose or 0) + Config.transpose
  local tokens = parseRPS(song.content)

  for idx, tok in ipairs(tokens) do
    if State.stopRequested then break end
    if tok.break_ then
      task.wait(Config.restGap)
    elseif tok.chord then
      local notes = charsToNotes(tok.text, transpose)
      if #notes > 0 then
        for _, n in ipairs(notes) do
          if State.stopRequested then break end
          playNote(n)
          if State.onNote then pcall(State.onNote, n) end
          task.wait(Config.chordGap)
        end
        task.wait(Config.noteGap)
      end
    elseif tok.note then
      local base = charToNote(tok.note)
      if base then
        local n = base + transpose
        if n >= 1 and n <= 61 then
          playNote(n)
          if State.onNote then pcall(State.onNote, n) end
        end
      end
      task.wait(Config.noteGap)
    end
  end

  State.playing = false
  State.currentSong = nil
  if Config.loopSong and not State.stopRequested then
    return playSongBySlug(slug)
  end
  return true
end

local function playByName(name)
  if State.playing then
    State.stopRequested = true
    repeat task.wait() until not State.playing
  end
  local slug = name
  -- allow fuzzy: match by title substring
  if not SONGS[slug] then
    for s, song in pairs(SONGS) do
      if s:lower():find(name:lower(), 1, true) or song.title:lower():find(name:lower(), 1, true) then
        slug = s
        break
      end
    end
  end
  if not SONGS[slug] then
    warn("[AutoPiano] Song not found: " .. tostring(name))
    print("[AutoPiano] Available songs:")
    for i, s in ipairs(SONG_LIST) do print(("  %d. %s (%s)"):format(i, s.title, s.slug)) end
    return false
  end
  print(("[AutoPiano] ▶ Playing: %s"):format(SONGS[slug].title))
  State.thread = task.spawn(function()
    local ok, err = pcall(playSongBySlug, slug)
    if not ok then warn("[AutoPiano] error: " .. tostring(err)) end
  end)
  return true
end

local function stop()
  State.stopRequested = true
  print("[AutoPiano] ■ Stopped")
end

local function listSongs()
  print("[AutoPiano] " .. #SONG_LIST .. " songs available:")
  for i, s in ipairs(SONG_LIST) do
    local t = SONGS[s.slug].transpose or 0
    print(("  %2d. %-30s (slug: %s, transpose %d)"):format(i, s.title, s.slug, t))
  end
end


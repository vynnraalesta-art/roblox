-- Virtual piano key map (identical to the game's own LetterToNote table).
-- Lowercase = natural, shifted chars = sharps/octave-up. Index = note number 1..61.
local KEYMAP = "1!2@34$5%6^78*9(0qQwWeErtTyYuiIoOpPasSdDfgGhHjJklLzZxcCvVbBnm"

-- Convert a single RPS character to a note number (1..61). Returns nil if invalid.
local function charToNote(ch)
  return string.find(KEYMAP, ch, 1, true)
end


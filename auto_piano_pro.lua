    -- approximate: faster bpm -> smaller gap. bpm maps inversely.
    Config.noteGap = math.clamp(60 / (bpm * 2), 0.05, 1.0)
    print(("[AutoPiano] noteGap set to %.3f (≈%d bpm)"):format(Config.noteGap, bpm))
  end,
}

